SELECT tbl1.ID_MUESTRA, tbl2.ID_MUESTRA
FROM PTOS_GeorrefParcel tbl1
INNER JOIN PTOS_GeorrefParcel tbl2 
ON ST_Equals(tbl1.Shape, tbl2.Shape) = 1
WHERE tbl1.ID_MUESTRA <> tbl2.ID_MUESTRA
ORDER BY tbl1.ID_MUESTRA





